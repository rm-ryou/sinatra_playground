module SessionsHelper
  def log_in(user)
    session[:user_id] = user.id
  end

  def current_user
    if session[:user_id]
      @current_user ||= User.find_by(id: session[:user_id])
    end
  end

  def logged_in?
    !current_user.nil?
  end

  def redirect_to_logged_in
    if logged_in?
      @user = current_user
    else
      redirect '/login'
    end
  end
end
